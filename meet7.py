# komparasi true  dan false
# ><= <== != is is not 
x = 25
y = 69

print("========== lebih dari (>)")
print(x > y) 
print(x, ">", y, "=", x > y) 

print("========== kurang dari (<)") 
print(x < y) 
print(x, "<", y, "=", x < y) 

print(">= lebih dari sama dengan") 
print(x >= y) 
print(x, ">=", y, "=", x >= y)

print("<= kurang dari sama dengan")
print(x <= y) 
print(x, "<=", y, "=", x <= y)

print("== sama dengan")
print(x == y) 
print(x, "==", y, "=", x == y)

print("!= tidak sama dengan") 
print(x != y) 
print(x, "!=", y, "=" , x != y) 